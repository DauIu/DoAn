    private void tabHopDongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabHopDongMouseClicked
        // TODO add your handling code here:
        int selectedRow = tabHopDong.getSelectedRow();
    if (selectedRow != -1) {  
        String maHD = tabHopDong.getValueAt(selectedRow, 0).toString();
        String maSV = tabHopDong.getValueAt(selectedRow, 1).toString();
        String trangThai = tabHopDong.getValueAt(selectedRow, 6).toString();

        txtMaHD.setText(maHD);
        txtMaSV.setText(maSV);
        cboTrangThai.setSelectedItem(trangThai);

        // Kiểm tra nếu trạng thái là "Xin gia hạn" thì bật nút btnXemGiaHan
        if (trangThai.equals("Xin gia hạn")) {
            btnXemGiaHan.setEnabled(true);
        } else {
            btnXemGiaHan.setEnabled(false);
        }
    }
    }//GEN-LAST:event_tabHopDongMouseClicked

