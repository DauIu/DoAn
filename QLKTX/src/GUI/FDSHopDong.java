    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed

    String maHopDong = txtMaHD.getText(); 
    String trangThaiMoi = cboTrangThai.getSelectedItem().toString(); 

    // Kiểm tra xem người dùng đã chọn hợp đồng nào chưa
    if (maHopDong.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn hợp đồng cần sửa!", "Thông báo", JOptionPane.WARNING_MESSAGE);
        return; 
    }

    HopDongDAO hopDongDAO = new HopDongDAO();
    boolean capNhatThanhCong = hopDongDAO.capNhatTrangThaiHopDong(maHopDong, trangThaiMoi);

    if (capNhatThanhCong) {
        JOptionPane.showMessageDialog(this, "Cập nhật trạng thái hợp đồng thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(this, "Cập nhật trạng thái hợp đồng không thành công!", "Thông báo", JOptionPane.ERROR_MESSAGE);
    }
    loaddHopDongData();
    }//GEN-LAST:event_btnSuaActionPerformed

