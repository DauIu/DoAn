private void loaddHopDongData() {
    HopDongDAO hopDongDAO = new HopDongDAO();
    List<HopDong> hopDongList = hopDongDAO.getAllHopDong();

    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("MaHopDong");
    model.addColumn("MaSV");
    model.addColumn("NguoiTao");
    model.addColumn("NgayTao");
    model.addColumn("NgayBatDau");
    model.addColumn("NgayKetThuc");
    model.addColumn("Trạng thái");

    for (HopDong hopDong : hopDongList) {
        model.addRow(new Object[]{hopDong.getMaHopDong(), hopDong.getMaSV(), hopDong.getNguoiTao(), hopDong.getNgayTao(), hopDong.getNgayBatDau(), hopDong.getNgayKetThuc(), hopDong.getTrangThai()});
    }

    tabHopDong.setModel(model);
}

